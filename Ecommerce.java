class Ecommerce{
	static  int eId;
	static String name;
	static String address;
	static String branch;
	// setter and getter
	
	
	static void setEcmId(int id){
		eId=id;
	}
	static int getEcmId(){
		return eId;
	}
	
	static void setName(String nm){
		name=nm;
	}
	static String getName(){
		return name;
	}
	
	static void setAddress(String addr){
		address=addr;
	}
	static String getAddress(){
		return address;
	}
	
	static void setBranch(String bh){
		branch=bh;
	}
	static String getBranch(){
		return branch;
	}
	
}